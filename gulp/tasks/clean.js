// Vars
// -----------------------------------------------------------------------------

var gulp = require('gulp'),
    del = require('del'),
    config = require('../config');


// Tasks
// -----------------------------------------------------------------------------

gulp.task('clean:build', function(cb) {
    del([
        config.dest.build
    ], cb);
})

gulp.task('clean:styles', function(cb) {
    del([
        config.css.build
    ], cb);
})
